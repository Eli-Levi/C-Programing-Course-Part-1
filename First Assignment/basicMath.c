#include "myMath.h"
float add (float x, float y)
{
    float sum = x+y;
    return sum;
}

float sub(float x, float y)
{
    float sub = x-y;
    return sub;
}

double mul(double x, int y)
{
    double multi = y*x;
    return multi;
}

double div(double x, int y)
{
    double divide = x/y;
    return divide;
}
